async function main() {
  try {
    const provider = new ethers.providers.JsonRpcProvider(process.env.URL);
    const signer = new ethers.Wallet(process.env.PRIVATE_KEY, provider);
    
    const testDEXStake = new ethers.Contract(testDEXStakeAddress, testDEXStakeABI, signer);
    const USDCToken = new ethers.Contract(TOKEN_A, USDC_ABI, signer);

    // 1. Kiểm tra số dư trước
    const decimals = await USDCToken.decimals();
    const balance = await USDCToken.balanceOf(signer.address);
    console.log("Current balance:", ethers.utils.formatUnits(balance, decimals), "USDC");

    // 2. Số lượng muốn deposit
    const amount = ethers.utils.parseUnits("0.000001", decimals);
    console.log("Amount to deposit:", ethers.utils.formatUnits(amount, decimals), "USDC");

    // 3. Kiểm tra allowance hiện tại
    const currentAllowance = await USDCToken.allowance(signer.address, testDEXStakeAddress);
    console.log("Current allowance:", ethers.utils.formatUnits(currentAllowance, decimals), "USDC");

    // 4. Approve nếu chưa đủ
    if (currentAllowance.lt(amount)) {
      console.log("Approving USDC...");
      const approveTx = await USDCToken.approve(testDEXStakeAddress, amount);
      await approveTx.wait();
      console.log("✅ Approved successfully!");
    } else {
      console.log("✅ Already approved!");
    }

    // 5. Thực hiện deposit
    console.log("Depositing to staking pool...");
    const depositTx = await testDEXStake.deposit(amount);
    const receipt = await depositTx.wait();

    if (receipt.status === 1) {
      console.log("✅ Deposit successful!");
      console.log("Transaction hash:", depositTx.hash);

      // 6. Kiểm tra thông tin sau deposit
      const userInfo = await testDEXStake.userInfo(signer.address);
      console.log("\nStaking Information:");
      console.log("- Staked amount:", ethers.utils.formatUnits(userInfo.amount, decimals), "USDC");
      console.log("- Reward debt:", ethers.utils.formatUnits(userInfo.rewardDebt, decimals));

      // 7. Kiểm tra số dư sau
      const newBalance = await USDCToken.balanceOf(signer.address);
      console.log("\nNew balance:", ethers.utils.formatUnits(newBalance, decimals), "USDC");
    } else {
      console.error("❌ Deposit failed!");
    }

  } catch (error) {
    console.error("Error:", error);
    // In chi tiết lỗi nếu có
    if (error.reason) {
      console.error("Error reason:", error.reason);
    }
    if (error.data) {
      console.error("Error data:", error.data);
    }
  }
}

main()
  .then(() => process.exit(0))
  .catch((error) => {
    console.error(error);
    process.exit(1);
  });


