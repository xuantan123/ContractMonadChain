async function main() {
    const provider = new ethers.providers.JsonRpcProvider(process.env.URL);
    const signer = new ethers.Wallet(process.env.PRIVATE_KEY, provider);
    
    const testDEXStake = new ethers.Contract(testDEXStakeAddress, testDEXStakeABI, signer);
    
    // Lấy địa chỉ admin từ signer
    const admin = signer.address;
    
    const stakedToken = "0xf817257fed379853cDe0fa4F97AB987181B1E5Ea"; //USDC
    const rewardToken = "0xf7E259629aFC7A1739C306D48B7Aee32b805A0dd";  //tDex(flatform)
    const rewardPerSecond = ethers.utils.parseEther("0.1");
    const startTimestamp = Math.floor(Date.now() / 1000) + 300;
    const bonusEndTimestamp = startTimestamp + (7 * 24 * 60 * 60);
    const poolLimitPerUser = ethers.utils.parseEther("10000");

    console.log("Initializing with parameters:");
    console.log("- Staked Token:", stakedToken);
    console.log("- Reward Token:", rewardToken);
    console.log("- Reward Per Second:", ethers.utils.formatEther(rewardPerSecond));
    console.log("- Start Time:", new Date(startTimestamp * 1000).toLocaleString());
    console.log("- End Time:", new Date(bonusEndTimestamp * 1000).toLocaleString());
    console.log("- Pool Limit Per User:", ethers.utils.formatEther(poolLimitPerUser));
    console.log("- Admin:", admin);

    try {
        const tx = await testDEXStake.initialize(
            stakedToken,
            rewardToken, 
            rewardPerSecond,
            startTimestamp,
            bonusEndTimestamp,
            poolLimitPerUser,
            admin
        );
        
        console.log("Transaction sent:", tx.hash);
        await tx.wait();
        console.log("Pool initialized successfully!");
        
        // Kiểm tra xác nhận khởi tạo
        const isInitialized = await testDEXStake.isInitialized();
        console.log("Pool initialization status:", isInitialized);
        
    } catch (error) {
        console.error("Error initializing pool:", error.message);
        throw error;
    }
}

main()
    .then(() => process.exit(0))
    .catch((error) => {
        console.error("❌ Error:", error);
        process.exit(1);
    });

