const mintContract = new ethers.Contract(MintNFTs, MintNFTs_ABI, signer);

async function checkContract() {
    try {
        const code = await provider.getCode(MintNFTs);
        console.log(`üìã Contract code length: ${code.length}`);
        
        if (code === "0x") {
            console.error("‚ùå No contract found at this address!");
            return false;
        }
        
        // Try to read contract name
        const name = await mintContract.name();
        console.log(`üìã Contract name: ${name}`);
        
        return true;
    } catch (error) {
        console.error("‚ùå Error checking contract:", error.message);
        return false;
    }
}

async function checkOwnership() {
    try {
        const owner = await mintContract.owner();
        const signerAddress = await signer.getAddress();
        
        console.log(`üìã Contract owner: ${owner}`);
        console.log(`üìã Signer address: ${signerAddress}`);
        console.log(`‚úÖ Is owner?: ${owner.toLowerCase() === signerAddress.toLowerCase()}`);
        
        return owner.toLowerCase() === signerAddress.toLowerCase();
    } catch (error) {
        console.error("‚ùå Error checking ownership:", error.message);
        return false;
    }
}

async function setRouterAndTest() {
    console.log("\nüîÑ Setting TabiSwap Router...");

    try {
        // Check contract exists
        const contractExists = await checkContract();
        if (!contractExists) return;
        
        // Check ownership
        const isOwner = await checkOwnership();
        if (!isOwner) {
            console.error("‚ùå You are not the contract owner!");
            return;
        }

        // Set router with manual gas limit
        console.log(`Setting router to: ${TABI_SWAP_ROUTER_ADDRESS}`);
        const setRouterTx = await mintContract.setTabiSwapRouter(TABI_SWAP_ROUTER_ADDRESS, {
            gasLimit: 100000
        });
        
        console.log(`üìã Set router transaction: ${setRouterTx.hash}`);
        await setRouterTx.wait();
        console.log("‚úÖ Router set successfully");
        
        // Verify router was set
        const currentRouter = await mintContract.tabiSwapRouter();
        console.log(`üîç Current router: ${currentRouter}`);
        
    } catch (error) {
        console.error("‚ùå Error:", error.message);
        console.log("Full error:", error);
    }
}

async function main() {
    try {
        await setRouterAndTest();
    } catch (error) {
        console.error("‚ùå Main error:", error.message);
    }
}

