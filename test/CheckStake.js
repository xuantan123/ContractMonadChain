async function checkUserStakeInfo() {
  try {
    // Connect to provider
    const provider = new ethers.providers.JsonRpcProvider(process.env.URL);
    const signer = new ethers.Wallet(process.env.PRIVATE_KEY, provider);

    const userAddress = "0xf39ead9D924336766e01167A52f4A23444F79Ef7";
    
    // Get contract instance
    const stakeContract = new ethers.Contract(STAKE_ADDRESS, STAKE_ABI, signer);

    console.log("\nðŸ” Checking stake info for:", userAddress);
    console.log("=======================================");

    // 1. Get basic user info
    const userInfo = await stakeContract.userInfo(userAddress);
    console.log("\nðŸ“Š Basic User Info:");
    console.log("----------------------------------------");
    console.log(`Shares: ${ethers.utils.formatEther(userInfo.shares)} shares`);
    console.log(`LastDepositedTime: ${ethers.utils.formatEther(userInfo.lastDepositedTime)} lastDepositedTime`)
    console.log(`Locked Amount: ${ethers.utils.formatEther(userInfo.lockedAmount)} tokens`);
    console.log(`Boosted Share: ${ethers.utils.formatEther(userInfo.userBoostedShare)} shares`);
    console.log(`Lock Status: ${userInfo.locked ? "ðŸ”’ Locked" : "ðŸ”“ Unlocked"}`);

    const lockStartTime = Number(userInfo.lockStartTime);
    const lockEndTime = Number(userInfo.lockEndTime);
    const now = Math.floor(Date.now() / 1000); // current timestamp in seconds
    const timeLeft = Math.max(lockEndTime - now, 0);

    console.log(`Lock Started: ${new Date(lockStartTime * 1000).toLocaleString()}`);
    console.log(`Lock Ends: ${new Date(lockEndTime * 1000).toLocaleString()}`);
    console.log(`Time Left: ${Math.floor(timeLeft / 86400)} days ${Math.floor((timeLeft % 86400) / 3600)} hrs`);

    // 2. Time information
    console.log("\nâ° Time Information:");
    console.log("----------------------------------------");
    console.log(`Last Deposited: ${new Date(userInfo.lastDepositedTime.toNumber() * 1000).toLocaleString()}`);
    console.log(`Last Action: ${new Date(userInfo.lastUserActionTime.toNumber() * 1000).toLocaleString()}`);
    
    if (userInfo.locked) {
      console.log(`Lock Started: ${new Date(userInfo.lockStartTime.toNumber() * 1000).toLocaleString()}`);
      console.log(`Lock Ends: ${new Date(userInfo.lockEndTime.toNumber() * 1000).toLocaleString()}`);
      const timeLeft = userInfo.lockEndTime.toNumber() - now;
      console.log(`Time Left: ${timeLeft > 0 ? Math.floor(timeLeft / 86400) + " days" : "Lock expired"}`);
    }

    // 3. Balance and Value Information
    console.log("\nðŸ’° Balance & Value Information:");
    console.log("----------------------------------------");
    const pricePerShare = await stakeContract.getPricePerFullShare();
    const userValue = userInfo.shares.mul(pricePerShare).div(ethers.constants.WeiPerEther);
    console.log(`Price Per Share: ${ethers.utils.formatEther(pricePerShare)}`);
    console.log(`Total Value: ${ethers.utils.formatEther(userValue)} tokens`);
    console.log(`Cake At Last Action: ${ethers.utils.formatEther(userInfo.cakeAtLastUserAction)} tokens`);

    // 4. Fee Information
    console.log("\nðŸ’¸ Fee Information:");
    console.log("----------------------------------------");
    const performanceFee = await stakeContract.calculatePerformanceFee(userAddress);
    const overdueFee = await stakeContract.calculateOverdueFee(userAddress);
    const withdrawFee = await stakeContract.calculateWithdrawFee(userAddress, userInfo.shares);
    
    console.log(`Performance Fee: ${ethers.utils.formatEther(performanceFee)} tokens`);
    console.log(`Overdue Fee: ${ethers.utils.formatEther(overdueFee)} tokens`);
    console.log(`Withdraw Fee: ${ethers.utils.formatEther(withdrawFee)} tokens`);

    // 5. Calculate Earnings
    console.log("\nðŸ“ˆ Earnings Information:");
    console.log("----------------------------------------");
    const currentValue = userValue.sub(performanceFee).sub(overdueFee).sub(withdrawFee);
    const initialValue = userInfo.cakeAtLastUserAction;
    const earnings = currentValue.sub(initialValue);
    
    console.log(`Current Value (After Fees): ${ethers.utils.formatEther(currentValue)} tokens`);
    console.log(`Initial Value: ${ethers.utils.formatEther(initialValue)} tokens`);
    console.log(`Total Earnings: ${ethers.utils.formatEther(earnings)} tokens`);

    return {
      userInfo: {
        shares: userInfo.shares.toString(),
        lockedAmount: userInfo.lockedAmount.toString(),
        userBoostedShare: userInfo.userBoostedShare.toString(),
        locked: userInfo.locked,
        lastDepositedTime: userInfo.lastDepositedTime.toString(),
        lastUserActionTime: userInfo.lastUserActionTime.toString(),
        lockStartTime: userInfo.lockStartTime.toString(),
        lockEndTime: userInfo.lockEndTime.toString(),
        cakeAtLastUserAction: userInfo.cakeAtLastUserAction.toString()
      },
      fees: {
        performanceFee: performanceFee.toString(),
        overdueFee: overdueFee.toString(),
        withdrawFee: withdrawFee.toString()
      },
      value: {
        pricePerShare: pricePerShare.toString(),
        totalValue: userValue.toString(),
        currentValue: currentValue.toString(),
        earnings: earnings.toString()
      }
    };

  } catch (error) {
    console.error("âŒ Error checking stake info:", error);
    return null;
  }
}

