async function main() {
    try {
        const provider = new ethers.providers.JsonRpcProvider(process.env.URL);
        const signer = new ethers.Wallet(process.env.PRIVATE_KEY, provider);
        
        const testDEXStake = new ethers.Contract(testDEXStakeAddress, testDEXStakeABI, signer);

        // 1. Kiểm tra thông tin user trước khi withdraw
        console.log("👤 Withdrawing from:", signer.address);
        const userInfo = await testDEXStake.userInfo(signer.address);
        console.log("\n📊 Current Staking Info:");
        console.log("- Total Shares:", ethers.utils.formatEther(userInfo.shares));
        console.log("- Locked Amount:", ethers.utils.formatEther(userInfo.lockedAmount));
        console.log("- Lock Status:", userInfo.locked);
        console.log("- Lock End Time:", new Date(userInfo.lockEndTime * 1000).toLocaleString());

        // 2. Kiểm tra xem có thể withdraw không
        if (userInfo.locked && userInfo.lockEndTime > Math.floor(Date.now() / 1000)) {
            console.log("\n❌ Cannot withdraw - Still in lock period");
            console.log("Lock ends:", new Date(userInfo.lockEndTime * 1000).toLocaleString());
            return;
        }

        // 3. Tính toán số shares muốn withdraw (ví dụ: withdraw 50% shares)
        const sharesToWithdraw = userInfo.shares.div(2); // Rút 50% shares
        console.log("\n🔄 Withdrawing shares:", ethers.utils.formatEther(sharesToWithdraw));

        // 4. Thực hiện withdraw với số shares cụ thể
        console.log("\n🔄 Processing withdrawal...");
        
        const feeData = await provider.getFeeData();
        const tx = await testDEXStake.withdraw(sharesToWithdraw, {
            gasLimit: ethers.utils.hexlify(500000),
            maxFeePerGas: feeData.maxFeePerGas,
            maxPriorityFeePerGas: feeData.maxPriorityFeePerGas
        });

        console.log(`📝 Transaction sent: ${tx.hash}`);
        await tx.wait();
        
        // 5. Kiểm tra lại số shares sau khi withdraw
        const updatedUserInfo = await testDEXStake.userInfo(signer.address);
        console.log("\n✅ Withdrawal successful!");
        console.log("Remaining shares:", ethers.utils.formatEther(updatedUserInfo.shares));

    } catch (error) {
        console.error("\n❌ Error:", error.reason || error.message);
    }
}

main()
    .then(() => process.exit(0))
    .catch(error => {
        console.error(error);
        process.exit(1);
    });


