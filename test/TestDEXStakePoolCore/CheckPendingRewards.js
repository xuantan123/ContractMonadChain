async function main() {
    try {
        const provider = new ethers.providers.JsonRpcProvider(process.env.URL);
        const wallet = new ethers.Wallet("c9e91c85bb23173d6847a699909cb2bbe4d23d5f116e0423a9e28a0036c7e5b1", provider);
        
        const stakingContract = new ethers.Contract(stakingAddress, stakingABI, wallet);
        const masterChefContract = new ethers.Contract(masterChefAddress, masterChefABI, wallet);
        
        const userAddress = wallet.address;
        console.log("Checking pending rewards for address:", userAddress);

        // 1. Lấy thông tin user
        const userInfo = await stakingContract.userInfo(userAddress);
        console.log("\nUser Info:");
        console.log("Shares:", ethers.utils.formatEther(userInfo.shares));
        console.log("Locked Amount:", ethers.utils.formatEther(userInfo.lockedAmount));
        console.log("Cake At Last Action:", ethers.utils.formatEther(userInfo.cakeAtLastUserAction));
        console.log("Lock Status:", userInfo.locked);
        
        if (userInfo.locked) {
            console.log("Lock End Time:", userInfo.lockEndTime.toNumber());
            console.log("lastDepositedTime:", userInfo.lastDepositedTime.toNumber());
            console.log("lastUserActionTime:", userInfo.lastUserActionTime.toNumber());
            console.log("lockStartTime:", userInfo.lockStartTime.toNumber());
        }

        // 2. Lấy thông tin pool
        const totalPendingRewards = await stakingContract.calculateTotalPendingCakeRewards();
        const totalPoolBalance = await stakingContract.balanceOf();
        const totalShares = await stakingContract.totalShares();

        console.log("\nPool Info:");
        console.log("Total Pool Balance:", ethers.utils.formatEther(totalPoolBalance));
        console.log("Total Pending Rewards:", ethers.utils.formatEther(totalPendingRewards));
        console.log("Total Shares:", ethers.utils.formatEther(totalShares));

        // Thêm: Kiểm tra thông tin farm
        console.log("\nFarm Configuration:");
        const cakePoolPID = await stakingContract.cakePoolPID();
        console.log("Pool ID in MasterChef:", cakePoolPID.toString());

        try {
            const poolInfo = await masterChefContract.poolInfo(cakePoolPID);
            console.log("Pool Alloc Point:", poolInfo.allocPoint.toString());
            console.log("Last Reward Block:", poolInfo.lastRewardBlock.toString());
            
            const totalAllocPoint = await masterChefContract.totalSpecialAllocPoint();
            console.log("Total Alloc Point:", totalAllocPoint.toString());
         

            // Check pending rewards trực tiếp từ MasterChef
            const pendingFromFarm = await masterChefContract.pendingCake(cakePoolPID, stakingAddress);
            console.log("\nPending Rewards from Farm:", ethers.utils.formatEther(pendingFromFarm));
        } catch (error) {
            console.log("Error getting farm info:", error.message);
        }

        // 3. Tính pending reward thủ công
        if (userInfo.shares.gt(0)) {
            const pool = totalPoolBalance.add(totalPendingRewards);
            const userAmount = pool.mul(userInfo.shares).div(totalShares);
            
            // Trừ các loại phí
            const performanceFee = await stakingContract.calculatePerformanceFee(userAddress);
            const overdueFee = await stakingContract.calculateOverdueFee(userAddress);
            
            let pendingReward = userAmount.sub(userInfo.cakeAtLastUserAction);
            pendingReward = pendingReward.sub(performanceFee).sub(overdueFee);

            console.log("\nCalculated Pending Reward:", ethers.utils.formatEther(pendingReward));
            console.log("Performance Fee:", ethers.utils.formatEther(performanceFee));
            console.log("Overdue Fee:", ethers.utils.formatEther(overdueFee));
        } else {
            console.log("\nNo shares, pending reward is 0");
        }

    } catch (error) {
        console.error("Error:", error);
    }
}

main()
    .then(() => process.exit(0))
    .catch((error) => {
        console.error(error);
        process.exit(1);
    });

