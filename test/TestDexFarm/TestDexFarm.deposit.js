async function main() {
  const provider = new ethers.providers.JsonRpcProvider(process.env.URL); // RPC c·ªßa TBSChain
  const signer = new ethers.Wallet(process.env.PRIVATE_KEY, provider); 

  const testDexFarm = new ethers.Contract(testDexFarmAddress, testDexFarmABI, signer);
  const lpToken = new ethers.Contract(lpTokenAddress, Pair_ABI, signer);

  const owner = await testDexFarm.owner();
  console.log("üëë Contract owner:", owner);
  console.log("üîë Your address:", signer.address);
  if (owner.toLowerCase() !== signer.address.toLowerCase()) {
    throw new Error("Signer is not the contract owner! Cannot add pool.");
  }

  let totalPools = await testDexFarm.poolLength();
  console.log(`üîç Total Pools Available: ${totalPools.toString()}`);

  const isWhitelisted = await testDexFarm.whiteList(signer.address);
  console.log(`üîç Are you on the whitelist? ${isWhitelisted}`);

  if (!isWhitelisted) {
    console.log("‚è≥ Adding to whitelist...");
    let tx = await testDexFarm.updateWhiteList(signer.address, true);
    await tx.wait();
    console.log("‚úÖ Added to whitelist!");
  }

  // Th√™m pool cho ƒë·∫øn khi c√≥ √≠t nh·∫•t 2 pool
  while (totalPools.toNumber() < 3) {
    console.log("‚è≥ Adding new pool...");
    const feeData = await provider.getFeeData();
        
    let tx = await testDexFarm.add(2, lpTokenAddress, true, false, {
      gasLimit: 500000,
      maxFeePerGas: feeData.maxFeePerGas || ethers.utils.parseUnits("20", "gwei"),
      maxPriorityFeePerGas: feeData.maxPriorityFeePerGas || ethers.utils.parseUnits("2", "gwei"),
    });
    await tx.wait();
    console.log("‚úÖ New Pool has been added!");

    // C·∫≠p nh·∫≠t l·∫°i totalPools
    totalPools = await testDexFarm.poolLength();
    console.log(`üîç Total Pools after adding: ${totalPools.toString()}`);
  }

  const pid = 2; 
  console.log(`üîç Checking Pool #${pid} Info...`);

  console.log(`‚è≥ Updating allocPoint of Pool #${pid} to 10...`);
  let tx = await testDexFarm.set(pid, 10 , true);
  await tx.wait();
  console.log("‚úÖ allocPoint updated to 10!");

  const pool = await testDexFarm.poolInfo(pid);
  console.log("pid:",pid);
  console.log(`   üü¢ allocPoint: ${pool.allocPoint.toString()}`);
  console.log(`   üü¢ lastRewardTimestamp: ${pool.lastRewardTimestamp.toString()}`);
  console.log(`   üü¢ accCakePerShare: ${pool.accCakePerShare.toString()}`);
  console.log(`   üü¢ totalBoostedShare: ${pool.totalBoostedShare.toString()}`);
  console.log(`   üü¢ isRegular: ${pool.isRegular}`);

  // const pool = await testDexFarm.poolInfo(2);
  // console.log("Pool #2 LP Token:", pool.lpToken);
  // console.log("lpTokenAddress b·∫°n ƒëang d√πng:", lpTokenAddress);

  console.log("‚è≥ Checking Boost Multiplier...");
  let boostMultiplier = await testDexFarm.getBoostMultiplier(signer.address, pid);
  console.log(`‚úÖ Boost Multiplier: ${boostMultiplier.toString()}`);

  const amount = ethers.utils.parseUnits("0.000001", 18);
  console.log(`‚è≥ Granting permission to send ${ethers.utils.formatUnits(amount, 18)} LP Token into testDexFarm...`);
  tx = await lpToken.approve(testDexFarmAddress, amount);
  await tx.wait();
  console.log("‚úÖ Permission granted!");
  let farmBalance = await lpToken.balanceOf(testDexFarmAddress);
  console.log(`üì¶ LP Token Balance in Farm: ${ethers.utils.formatUnits(farmBalance, 18)}`);


  console.log("‚è≥ Sending LP tokens to farm...");
  const feeData = await provider.getFeeData();
  const maxFeePerGas = feeData.maxFeePerGas ? feeData.maxFeePerGas.mul(2) : ethers.utils.parseUnits("30", "gwei");
  const maxPriorityFeePerGas = feeData.maxPriorityFeePerGas ? feeData.maxPriorityFeePerGas.mul(2) : ethers.utils.parseUnits("3", "gwei");

  tx = await testDexFarm.deposit(pid, amount, {
  gasLimit: 1000000,
  maxFeePerGas,
  maxPriorityFeePerGas,
  });
  await tx.wait();
  console.log("‚úÖ Sent successfully!");

  console.log("‚è≥ Checking Pool Data after deposit...");
}


